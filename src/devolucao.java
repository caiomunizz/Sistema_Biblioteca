    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String IDLivro=jTextField1.getText();
        String IDEstudante=jTextField2.getText();
        try{
            Connection con=ConnectionProvider.getCon();
            Statement st=con.createStatement();
            ResultSet rs=st.executeQuery("select *from consulta where IDLivro='"+IDLivro+"' and IDEstudante='"+IDEstudante+"'");
            if(rs.next()){
                jTextField3.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField1.setEditable(false);
                jTextField2.setEditable(false);
            }
            else{
                JOptionPane.showMessageDialog(null, "O livro n√£o foi alugado pelo aluno");
                setVisible(false);
                new devolucao().setVisible(true);
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null, "Conection Erro");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

